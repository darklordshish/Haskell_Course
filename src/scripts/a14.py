def main():
    with open(PATH, encoding="utf-8") as f:
        nb = json.load(f)
    applied = 0
    for c in nb["cells"]:
        if c["cell_type"] != "markdown":
            continue
        s = "".join(c["source"]); orig = s
        if FIX10_OLD in s:
            s = s.replace(FIX10_OLD, FIX10_NEW, 1)
        for pref, beats in BEATS.items():
            if pref in s and "### Пример из жизни" not in s and "### Единая картина" not in s:
                s = s.rstrip("\n") + "\n" + beats
                applied += 1
                break
        if s != orig:
            c["source"] = s.splitlines(keepends=True)
    if applied < 12:
        print(f"ERROR: only {applied} sections matched", file=sys.stderr); sys.exit(1)
    with open(PATH, "w", encoding="utf-8") as f:
        json.dump(nb, f, ensure_ascii=False, indent=1); f.write("\n")
    print(f"adjunctions beats appended: {applied}")

if __name__ == "__main__":
    main()
